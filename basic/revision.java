public static int BinarySearch( int num [] , int key){
    int start = 0;
    int end = num.length-1;
    System.out.println("Length is = "+ end);

    while (start <= end) {
        int mid = (start+end)/2;

        if (num[mid] == key) {
            return mid;
        }
        if (num[mid] < key) {
            start = mid+1;
        }
        else{
            end = mid-1;
        }
    }
    return -1;
}

public static void Reverse(int num []){
    int first = 0;
    int last = num.length-1;

    while(first < last){
        int temp = num[last];
        num[last] = num[first];
        num[first] = temp;

        first++;
        last--;
    }
}

