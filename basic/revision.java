public static int Factorial( int num){
    int fact = 1; 
    for( int i = 1; i<= num ; i++){
        fact = fact*i;
        // System.out.println(fact);
    }
    return fact;
}
public static int binomial(int n , int r){
    int n_fact = Factorial(n);
    int r_fact = Factorial(r);
    int n_r_fact = Factorial(n-r);

    int bio = n_fact/(r_fact*n_r_fact);
    return bio;
}

